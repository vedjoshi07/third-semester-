class pr2_4 {

    public static void main(String args[])
    {
        int a = Integer.parseInt(args[0]);
        
        if (a % 2 == 0) 
        {

            System.out.println(a + " is even");

        } 
        else 
        {
            System.out.println(a + " is odd");
        }

    }
}
